    @Test
    fun `cpu should execute correctly`() {
        val commands = listOf(
            Cpu.Command.NoOp,
            Cpu.Command.Add(3),
            Cpu.Command.Add(-5)
        )

        val cpu = Cpu()
        cpu.execute(commands)

        cpu.tick { assertEquals(Cpu.Data(register = 1, cycle = 1), it) }
        cpu.tick { assertEquals(Cpu.Data(register = 1, cycle = 2), it) }
        cpu.tick { assertEquals(Cpu.Data(register = 1, cycle = 3), it) }
        cpu.tick { assertEquals(Cpu.Data(register = 4, cycle = 4), it) }
        cpu.tick { assertEquals(Cpu.Data(register = 4, cycle = 5), it) }
        cpu.tick { assertEquals(Cpu.Data(register = -1, cycle = 6), it) }
        cpu.tick { assertEquals(Cpu.Data(register = -1, cycle = 7), it) }
    }
